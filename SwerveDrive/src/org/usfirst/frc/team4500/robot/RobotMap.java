package org.usfirst.frc.team4500.robot;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	public final static double MAX_VOLTS = 4.95;
	
	public final static int brSpeedMotor = 0;
	public final static int brAngleMotor = 0;
	public final static int brEncoder = 0;
	
	public final static int blSpeedMotor = 0;
	public final static int blAngleMotor = 0;
	public final static int blEncoder = 0;
	
	public final static int frSpeedMotor = 0;
	public final static int frAngleMotor = 0;
	public final static int frEncoder = 0;
	
	public final static int flSpeedMotor = 0;
	public final static int flAngleMotor = 0;
	public final static int flEncoder = 0;
}